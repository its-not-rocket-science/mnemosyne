class LatinPlugin:
    language_code = "la"
    display_name  = "Latin (Classical — with morphological annotations)"
    direction     = "ltr"
    capabilities  = LanguageCapabilities(
        code="la",
        display_name="Latin (Classical — with morphological annotations)",
        direction="ltr",
        script_family="latin",
        tokenization_mode="whitespace",
        morphology_depth="shallow",
        lesson_modes_supported=["morphology", "vocabulary", "dictionary"],
        analysis_depth="morphology_light",
        segmentation_quality="medium",
        tokenization_quality="medium",
        morphology_quality="medium",
        syntax_support=False,
        idiom_detection=False,
        tts_lang_tag="la",
        transliteration_scheme=None,
        tense_pool=["present", "imperfect", "perfect", "pluperfect", "future", "future_perfect"],
        mood_pool=["indicative", "subjunctive", "imperative", "infinitive", "participle"],
        nuance_capabilities=NuanceCapabilities(
            idioms="none",
            phrase_families="none",
            literary_references="none",
            cultural_references="none",
            etymology="none",
            formality_register="none",
            grammar_nuance="none",
            pronunciation_tts="stub",
            transliteration="none",
            proverb_tradition="none",
            classical_or_scriptural_allusion="none",
            notes=(
                "Verb forms annotated as conjugation type when found in ITTB morph index "
                "(~3 400 forms). Imperfect/future active and infinitive endings also "
                "detected via suffix rules with moderate confidence. Prepositions, "
                "conjunctions, and particles emitted as grammar type. "
                "Forms outside ITTB fall back to suffix or dictionary mode."
            ),
        ),
    )

    def __init__(self) -> None:
        self.lesson_store: dict[str, CandidateObject] = {}

    def analyze_text(self, text: str) -> list[CandidateSentenceResult]:
        return [self.analyze_sentence(s) for s in self.split_sentences(text)]

    def split_sentences(self, text: str) -> list[str]:
        return [m.group(0).strip() for m in _SENTENCE_RE.finditer(text) if m.group(0).strip()]

    def analyze_sentence(self, sentence: str) -> CandidateSentenceResult:
        candidates: list[CandidateObject] = []
        seen: set[str] = set()

        for raw in sentence.split():
            token = _STRIP_PUNCT.sub("", raw)
            if not token:
                continue
            key = normalize_latin(token)
            if not key or key in seen:
                continue
            seen.add(key)

            entry       = _lookup(key)
            morph_entry = _morph().get(key)
            morph_pos   = morph_entry.get("pos") if morph_entry else None
            # Distinguish citation-form hits from inflection-resolved hits.
            is_lemma = (key in _CURATED) or (key in _lemmas())

            if morph_pos in _VERB_POS:
                lesson_data: dict[str, Any] = {"surface_form": token}
                if entry:
                    lesson_data["citation_form"] = entry["citation"]
                    lesson_data["gloss"]         = entry["gloss"]
                    lesson_data["grammar_note"]  = entry.get("grammar_note", "")
                for field in _MORPH_FIELDS:
                    if field in morph_entry:
                        lesson_data[field] = morph_entry[field]
                morph_tag = ":".join(
                    f"{f}={morph_entry[f]}" for f in sorted(_MORPH_FIELDS) if f in morph_entry
                )
                candidates.append(CandidateObject(
                    canonical_form=f"{key}:{morph_tag}" if morph_tag else key,
                    surface_form=token,
                    type="conjugation",
                    label=token,
                    lesson_data=lesson_data,
                    confidence=0.80,
                ))

            elif entry is not None and is_lemma:
                pos      = entry["pos"]
                obj_type = "grammar" if pos in _GRAMMAR_POS else "vocabulary"
                lesson_data = {
                    "lemma":         entry["citation"],
                    "citation_form": entry["citation"],
                    "gloss":         entry["gloss"],
                    "grammar_note":  entry.get("grammar_note", ""),
                    "pos":           pos.upper(),
                }
                if morph_entry:
                    for field in _MORPH_FIELDS:
                        if field in morph_entry:
                            lesson_data[field] = morph_entry[field]
                    confidence: float | None = 0.80
                else:
                    lesson_data["confidence_note"] = _DICT_ONLY_NOTE
                    confidence = 0.70
                candidates.append(CandidateObject(
                    canonical_form=key,
                    surface_form=token,
                    type=obj_type,
                    label=token,
                    lesson_data=lesson_data,
                    confidence=confidence,
                ))

            elif entry is not None and not is_lemma and entry["pos"] == "verb":
                # Inflection-resolved verb: only emit conjugation if we have
                # tense+mood (C9 contract).  Suffix rules cover imperfect/future/
                # infinitives; forms without a match fall back to vocabulary.
                verb_morph = _extract_latin_verb_morph(key)
                lesson_data = {
                    "citation_form": entry["citation"],
                    "gloss":         entry["gloss"],
                    "grammar_note":  entry.get("grammar_note", ""),
                    "surface_form":  token,
                }
                if verb_morph:
                    lesson_data.update(verb_morph)
                    lesson_data["confidence_note"] = _SUFFIX_NOTE
                    morph_tag = ":".join(f"{k}={v}" for k, v in sorted(verb_morph.items()))
                    candidates.append(CandidateObject(
                        canonical_form=f"{key}:{morph_tag}",
                        surface_form=token,
                        type="conjugation",
                        label=token,
                        lesson_data=lesson_data,
                        confidence=0.55,
                    ))
                else:
                    lesson_data["lemma"]           = entry["citation"]
                    lesson_data["confidence_note"] = _DICT_ONLY_NOTE
                    candidates.append(CandidateObject(
                        canonical_form=key,
                        surface_form=token,
                        type="vocabulary",
                        label=token,
                        lesson_data=lesson_data,
                        confidence=0.50,
                    ))

            elif entry is not None:
                # Inflection-resolved non-verb (noun/adj/etc.)
                pos      = entry["pos"]
                obj_type = "grammar" if pos in _GRAMMAR_POS else "vocabulary"
                lesson_data = {
                    "lemma":         entry["citation"],
                    "citation_form": entry["citation"],
                    "gloss":         entry["gloss"],
                    "grammar_note":  entry.get("grammar_note", ""),
                    "pos":           pos.upper(),
                    "confidence_note": _DICT_ONLY_NOTE,
                }
                if morph_entry:
                    for field in _MORPH_FIELDS:
                        if field in morph_entry:
                            lesson_data[field] = morph_entry[field]
                    confidence = 0.70
                else:
                    # No morph entry — add noun suffix hint if applicable
                    noun_hint = _extract_latin_noun_suffix_hint(key)
                    if noun_hint:
                        hint_feats, amb_note = noun_hint
                        if "case" in hint_feats and "case" not in lesson_data:
                            lesson_data["case_hint"]   = hint_feats["case"]
                        if "number" in hint_feats and "number" not in lesson_data:
                            lesson_data["number_hint"] = hint_feats["number"]
                        if "gender" in hint_feats and "gender" not in lesson_data:
                            lesson_data["gender_hint"] = hint_feats["gender"]
                        if amb_note:
                            lesson_data["ambiguity_note"] = amb_note
                        lesson_data["confidence_note"] = _NOUN_SUFFIX_NOTE
                    confidence = 0.60
                candidates.append(CandidateObject(
                    canonical_form=key,
                    surface_form=token,
                    type=obj_type,
                    label=token,
                    lesson_data=lesson_data,
                    confidence=confidence,
                ))

            elif morph_entry:
                lesson_data = {"lemma": key, "confidence_note": _UNKNOWN_NOTE}
                for field in _MORPH_FIELDS:
                    if field in morph_entry:
                        lesson_data[field] = morph_entry[field]
                candidates.append(CandidateObject(
                    canonical_form=key,
                    surface_form=token,
                    type="vocabulary",
                    label=token,
                    lesson_data=lesson_data,
                    confidence=0.50,
                ))

            else:
                lesson_data = {"lemma": key, "confidence_note": _UNKNOWN_NOTE}
                noun_hint = _extract_latin_noun_suffix_hint(key)
                if noun_hint:
                    hint_feats, amb_note = noun_hint
                    if "case" in hint_feats:
                        lesson_data["case_hint"]   = hint_feats["case"]
                    if "number" in hint_feats:
                        lesson_data["number_hint"] = hint_feats["number"]
                    if "gender" in hint_feats:
                        lesson_data["gender_hint"] = hint_feats["gender"]
                    if amb_note:
                        lesson_data["ambiguity_note"] = amb_note
                    lesson_data["confidence_note"] = _NOUN_SUFFIX_NOTE
                candidates.append(CandidateObject(
                    canonical_form=key,
                    surface_form=token,
                    type="vocabulary",
                    label=token,
                    lesson_data=lesson_data,
                    confidence=None,
                ))

        return CandidateSentenceResult(text=sentence, candidates=candidates)

    def get_lesson(self, object_id: str) -> CandidateObject | None:
        return self.lesson_store.get(object_id)


def create_plugin() -> LatinPlugin:
    return LatinPlugin()


