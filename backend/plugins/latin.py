# ── Lexicon loaders ───────────────────────────────────────────────────────────

@lru_cache(maxsize=1)
def _lemmas() -> dict[str, Any]:
    path = _DATA / "la_lemmas.json"
    if not path.exists():
        return {}
    return json.loads(path.read_text("utf-8"))["entries"]


@lru_cache(maxsize=1)
def _inflections() -> dict[str, str]:
    path = _DATA / "la_inflections.json"
    if not path.exists():
        return {}
    return json.loads(path.read_text("utf-8"))


@lru_cache(maxsize=1)
def _morph() -> dict[str, dict]:
    """Morphological index: normalised_form → {lemma, pos, case, number, …}."""
    path = _DATA / "la_morph.json"
    if not path.exists():
        return {}
    return json.loads(path.read_text("utf-8"))["entries"]


def _lookup(key: str) -> dict[str, str] | None:
    """Curated → kaikki lemma → kaikki inflection → None."""
    if key in _CURATED:
        return _CURATED[key]
    lem = _lemmas()
    entry = lem.get(key)
    if entry:
        return entry
    lemma_key = _inflections().get(key)
    if lemma_key:
        return lem.get(lemma_key)
    return None


# ── Morphological feature keys surfaced in lesson_data ───────────────────────
_MORPH_FIELDS = ("case", "number", "gender", "tense", "mood", "voice",
                 "person", "aspect", "verbform")

# ── Unknown-token notes ───────────────────────────────────────────────────────

_UNKNOWN_NOTE = (
    "Latin: form not in lexicon or morphological index. "
    "Inflected forms outside the ITTB corpus cannot be annotated. "
    "Use CLTK or Whitaker's Words for full morphological parsing."
)

_DICT_ONLY_NOTE = (
    "Latin dictionary mode: lexical entry found but morphological features "
    "not available for this specific inflected form."
)


