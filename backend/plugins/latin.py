"""Latin (Classical) plugin — dictionary-mode with morphological annotations.

BCP-47 code ``"la"``.  Provides whitespace tokenisation backed by:

  1. A compact curated set of ~90 high-priority headwords (always available).
  2. A 65 000-entry Wiktionary-derived lexicon from
     ``data/lexicons/la_lemmas.json`` (generated by ``scripts/ingest_kaikki.py``).
  3. An 828 000-entry inflection table from ``data/lexicons/la_inflections.json``
     that maps normalised inflected forms back to their lemma keys.
  4. A 3 400-entry morphological index from ``data/lexicons/la_morph.json``
     (generated by ``scripts/ingest_classical_morph.py`` from the Index
     Thomisticus Treebank dev split; provides case, number, gender, tense,
     mood, voice, person for annotated forms).

Lookup order per token:
  curated  →  kaikki lemma  →  kaikki inflection  →  unknown.
  Morphological features overlaid from la_morph.json when available.

Sources
────────
  la_morph.json is derived from la_ittb-ud-dev.conllu
  (Index Thomisticus Treebank, CC BY-NC-SA 3.0, Universal Dependencies).
  See scripts/ingest_classical_morph.py for derivation details.

Known limitations
─────────────────
  Morphological annotations cover ~3 400 forms (ITTB dev set only).
  The full ITTB treebank would give far better coverage; re-running
  ingest_classical_morph.py with the full dataset expands coverage.
  Forms outside the annotated corpus fall back to dictionary-mode output.
  No paradigm inference.
"""
