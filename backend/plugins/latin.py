"""Latin (Classical) plugin — dictionary-mode with morphological annotations.

BCP-47 code ``"la"``.  Provides whitespace tokenisation backed by:

  1. A compact curated set of ~90 high-priority headwords (always available).
  2. A 65 000-entry Wiktionary-derived lexicon from
     ``data/lexicons/la_lemmas.json`` (generated by ``scripts/ingest_kaikki.py``).
  3. An 828 000-entry inflection table from ``data/lexicons/la_inflections.json``
     that maps normalised inflected forms back to their lemma keys.
  4. A ~257 000-entry morphological index from ``data/lexicons/la_morph.json``
     (~254 000 noun/adj paradigm forms from kaikki.org Wiktionary data plus
     ~3 400 verb/misc forms from the Index Thomisticus Treebank dev split;
     provides case, number, gender, tense, mood, voice, person for annotated
     forms).  Generated by scripts/ingest_kaikki_la_morph.py and
     scripts/ingest_classical_morph.py.

Lookup order per token:
  curated  →  kaikki lemma  →  kaikki inflection  →  unknown.
  Morphological features overlaid from la_morph.json when available.
  Verb forms not in la_morph.json fall back to suffix-rule heuristics;
  noun/adj forms not in la_morph.json fall back to ending-pattern hints.

Sources
────────
  la_morph.json noun/adj data: kaikki_la.jsonl.gz
  (CC BY-SA 3.0 / GFDL, kaikki.org / Wiktionary paradigm tables).
  la_morph.json verb/misc data: la_ittb-ud-dev.conllu
  (Index Thomisticus Treebank, CC BY-NC-SA 3.0, Universal Dependencies).

Known limitations
─────────────────
  Noun/adjective morphological coverage is now broad (~220 000 forms).
  Verb morphological coverage remains limited to the ITTB dev set (~3 400
  forms); irregular and rare verb forms fall back to suffix heuristics.
  Forms outside the annotated corpus fall back to dictionary-mode output.
  No paradigm inference.
"""
