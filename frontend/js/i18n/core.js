/** Mutable per-language lookup table. Extended in place by loadBundle(). */
export const _table = {}
for (const lang of SUPPORTED) _table[lang] = { ...CORE_STRINGS[lang] }

// Mutable merge-targets for the three secondary maps that originally lived
// as standalone exports in js/i18n.js. Seeded empty here and populated in
// place by js/i18n/index.js's loadBundle('annotations'|'library') once the
// owning bundle resolves — consumers (js/modes/lesson.js,
// js/recommended-reading.js) hold a reference to these same objects via
// js/i18n/index.js's re-export, so no call-site changes are needed: the
// object identity never changes, only its contents, once loaded.
export const _annotationAria   = {}
export const _typeLabelsLong   = {}
export const _recommendUi      = {}
for (const lang of SUPPORTED) {
  _annotationAria[lang] = {}
  _typeLabelsLong[lang] = {}
  _recommendUi[lang]    = {}
}

// ── Runtime state ─────────────────────────────────────────────────────────────

let _currentLang = 'en'

/**
 * Translate a key in the currently-active UI language.
 * Falls back to English, then the key itself, so missing keys are visible
 * rather than throwing — this also covers the case where a lazy bundle
 * hasn't loaded yet: the raw key is shown rather than "undefined", and
 * once the owning bundle's loadBundle() resolves and the UI re-renders,
 * the real string takes over.
 */
export function t(key) {
  return _table[_currentLang]?.[key] ?? _table.en?.[key] ?? key
}

/** Return the currently-active UI language code (e.g. "fr", "de"). */
export function currentUiLang() {
  return _currentLang
}

/** Translate a key and substitute {placeholder} tokens from vars. */
export function ti(key, vars) {
  let s = t(key)
  for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, v)
  return s
}

/**
 * Apply translations to all annotated DOM elements and update the document
 * language and direction. Persists the choice to localStorage.
 */
export function applyUiLanguage(code) {
  const lang     = SUPPORTED.has(code) ? code : 'en'
  _currentLang   = lang

  const meta = UI_LANGUAGES.find(l => l.code === lang) ?? UI_LANGUAGES[0]
  document.documentElement.lang = lang
  document.documentElement.dir  = meta.dir

  // data-i18n → textContent
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n)
    if (val) el.textContent = val
  })

  // data-i18n-placeholder → placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.dataset.i18nPlaceholder)
    if (val) el.placeholder = val
  })

  // data-i18n-aria-label → aria-label attribute
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const val = t(el.dataset.i18nAriaLabel)
    if (val) el.setAttribute('aria-label', val)
  })

  // data-lesson-lang → textContent (lesson language select options)
  document.querySelectorAll('[data-lesson-lang]').forEach(el => {
    const val = t('lesson_lang_' + el.dataset.lessonLang)
    if (val && val !== 'lesson_lang_' + el.dataset.lessonLang) el.textContent = val
  })

  localStorage.setItem(LS_KEY, lang)

  // Keep the switcher select in sync when called programmatically.
  const sel = document.querySelector('#ui-language')
  if (sel && sel.value !== lang) sel.value = lang

  document.dispatchEvent(new CustomEvent('mnemosyne:language-changed', { detail: { lang } }))
}

/**
 * Initialise the UI language system. Detects browser language, populates
 * the #ui-language select, and applies the initial translations.
 * Must be called once on page load, before other rendering.
 */
export function initUiLanguage() {
  // Resolve language: localStorage → best browser match → 'en'
  const stored  = localStorage.getItem(LS_KEY)
  const browser = (navigator.languages ?? [navigator.language])
    .map(l => l.split('-')[0])
    .find(l => SUPPORTED.has(l))

  const initial = SUPPORTED.has(stored) ? stored
                : browser               ? browser
                : 'en'

  // Populate the language switcher.
  const sel = document.querySelector('#ui-language')
  if (sel) {
    sel.replaceChildren(
      ...UI_LANGUAGES.map(({ code, name }) => {
        const opt = document.createElement('option')
        opt.value       = code
        opt.textContent = name
        return opt
      })
    )
    sel.value = initial
    sel.addEventListener('change', () => applyUiLanguage(sel.value))
  }

  applyUiLanguage(initial)
}

