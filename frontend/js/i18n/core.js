/**
 * js/i18n/core.js — synchronous core translation bundle.
 *
 * Imported directly by js/main.js at startup (NOT lazy-loaded) — covers
 * auth, navigation chrome, the parse/picker form, offline-queue badge,
 * about/GDPR/shortcuts dialog content, and any string needed before a
 * lesson has been parsed or a route other than the default has been
 * visited.
 *
 * Also exports CAPABILITY_LABELS_I18N here rather than in annotations.js
 * (where its dp_-adjacent prefix would suggest it belongs): js/modes/
 * explorer.js reads it synchronously while building the capability chip
 * during initial language selection, before any lesson is parsed and long
 * before the 'lesson'/'annotations' bundles load — keeping it lazy would
 * risk a raw-key flash in the picker dialog.
 *
 * t()/ti()/initUiLanguage() keep their original signatures — every existing
 * call site across the app continues to work unchanged. The lookup table
 * (_table) is intentionally mutable and exported so js/i18n/index.js's
 * loadBundle() can merge additional bundles' strings into it after lazy
 * load, without requiring t()/ti() to change.
 *
 * Part of Session 5 of the frontend refactor (split of the former
 * 9181-line js/i18n.js).
 */

